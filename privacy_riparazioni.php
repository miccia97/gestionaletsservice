    <p>I dati raccolti per la gestione delle riparazioni (anagrafica cliente, telefono, indirizzo email, dettagli riparazione, costi, ecc.) sono trattati per:</p>
    <ul>
      <li>Eseguire le prestazioni di riparazione e relative comunicazioni.</li>
      <li>Gestire fatturazione e pagamenti.</li>
      <li>Assistenza post-riparazione e garanzia.</li>
      <li>Adempimenti fiscali e contabili.</li>
    </ul>

    <h2>3. Base Giuridica</h2>
    <p>Il trattamento è necessario per l’esecuzione di un contratto (Art. 6 c.1 b GDPR) e per adempiere obblighi legali (Art. 6 c.1 c GDPR).</p>

    <h2>4. Tipi di Dati Trattati</h2>
    <ul>
      <li>Dati identificativi del cliente (nome, cognome, contatti).</li>
      <li>Dettagli sulla riparazione (descrizione guasto, diagnosi, interventi).</li>
      <li>Informazioni economiche (costo effettivo, pagamenti).</li>
      <li>Dati di fatturazione e documentazione correlata.</li>
    </ul>

    <h2>5. Modalità del Trattamento</h2>
    <p>I dati sono trattati in forma manuale e informatica, con misure di sicurezza idonee a garantire integrità, riservatezza e disponibilità.</p>

    <h2>6. Conservazione</h2>
    <p>I dati saranno conservati per il periodo strettamente necessario all’evasione delle prestazioni e per obblighi di legge (fino a 10 anni per finalità fiscali).</p>

    <h2>7. Comunicazione dei Dati</h2>
    <p>I dati potranno essere comunicati a:</p>
    <ul>
      <li>Fornitori di servizi IT e software gestionali.</li>
      <li>Commercialisti e consulenti fiscali.</li>
      <li>Autorità giudiziarie o di controllo, se richiesto dalla legge.</li>
    </ul>

